function init() {
  const grid = document.getElementById('jobGrid');
  JOBS.forEach(job => {
    const card = document.createElement('div');
    card.className = 'job-card';
    card.id = `job-${job.id}`;
    card.onclick = () => selectJob(job.id);
    card.innerHTML = `<span class="job-emoji">${job.emoji}</span><div class="job-name">${job.name}</div><div class="job-tag">${job.tag}</div>`;
    grid.appendChild(card);
  });
}

function selectJob(id) {
  selectedJob = id;
  document.querySelectorAll('.job-card').forEach(c => c.classList.remove('active'));
  document.getElementById(`job-${id}`).classList.add('active');
  document.getElementById('toneSection').style.display = '';
  document.getElementById('generateSection').style.display = '';
  document.getElementById('outputSection').style.display = 'none';
  document.getElementById('toneSection').scrollIntoView({ behavior: 'smooth', block: 'nearest' });
}


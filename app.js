let selectedJob = null;
let selectedTone = 'sarcastic';

function init() {
  const grid = document.getElementById('jobGrid');
  JOBS.forEach(job => {
    const card = document.createElement('div');
    card.className = 'job-card';
    card.id = `job-${job.id}`;
    card.onclick = () => selectJob(job.id);
    card.innerHTML = `<span class="job-emoji">${job.emoji}</span><div class="job-name">${job.name}</div><div class="job-tag">${job.tag}</div>`;
    grid.appendChild(card);
  });
}

function selectJob(id) {
  selectedJob = id;
  document.querySelectorAll('.job-card').forEach(c => c.classList.remove('active'));
  document.getElementById(`job-${id}`).classList.add('active');
  document.getElementById('toneSection').style.display = '';
  document.getElementById('generateSection').style.display = '';
  document.getElementById('outputSection').style.display = 'none';
  document.getElementById('toneSection').scrollIntoView({ behavior: 'smooth', block: 'nearest' });
}

function selectTone(btn, tone) {
  selectedTone = tone;
  document.querySelectorAll('.tone-btn').forEach(b => b.classList.remove('active'));
  btn.classList.add('active');
}

function shuffle(arr) {
  return [...arr].sort(() => Math.random() - 0.5);
}

function generate() {
  if (!selectedJob) return;
  const btn = document.getElementById('generateBtn');
  btn.classList.add('loading');
  btn.querySelector('.btn-text').textContent = '生成中…';

  setTimeout(() => {
    btn.classList.remove('loading');
    btn.querySelector('.btn-text').textContent = '生成反驳话术';

    const job = JOBS.find(j => j.id === selectedJob);
    const pool = shuffle(REBUTTALS[selectedJob]).slice(0, 3);
    const extras = DARK_EXTRAS[selectedTone];

    const section = document.getElementById('outputSection');
    const meta = document.getElementById('outputMeta');
    const content = document.getElementById('outputContent');

    meta.textContent = `${job.emoji} ${job.name} · ${getToneLabel(selectedTone)} · ${pool.length}条话术`;
    content.innerHTML = '';

    pool.forEach((rb, i) => {
      const block = document.createElement('div');
      block.className = 'rebuttal-block';
      const bodyContent = selectedTone === 'professional' ? rb.professional : rb.dark;
      let extraHtml = '';
      if (selectedTone === 'sarcastic') {
        extraHtml = `<div class="rb-dark" style="border-color:#06b6d4;background:rgba(6,182,212,.06);color:#67e8f9;">💬 统计补充：持有此观点的管理层，通常自己岗位的AI替代率也在快速上升中。</div>`;
      }
      block.innerHTML = `
        <div class="rb-index">${i + 1}</div>
        <div class="rb-title">${rb.title}</div>
        <div class="rb-body">${bodyContent}</div>
        ${extraHtml}`;
      content.appendChild(block);
    });

    if (extras.length) {
      const extra = document.createElement('div');
      extra.style.cssText = 'margin-top:16px;padding:14px;border:1px dashed rgba(124,58,237,.3);border-radius:12px;font-size:13px;color:#a78bfa;line-height:1.8;';
      extra.textContent = extras[Math.floor(Math.random() * extras.length)];
      content.appendChild(extra);
    }

    section.style.display = '';
    section.scrollIntoView({ behavior: 'smooth' });
  }, 600);
}

function getToneLabel(tone) {
  return { professional: '专业严肃', sarcastic: '阴阳怪气' }[tone];
}

function copyAll() {
  const job = JOBS.find(j => j.id === selectedJob);
  const blocks = document.querySelectorAll('.rebuttal-block');
  let text = `【AI替代论反驳话术】${job.name} - ${getToneLabel(selectedTone)}\n\n`;
  blocks.forEach((b, i) => {
    const title = b.querySelector('.rb-title')?.textContent || '';
    const body = b.querySelector('.rb-body')?.textContent || '';
    const dark = b.querySelector('.rb-dark')?.textContent || '';
    text += `${i + 1}. ${title}\n${body}\n${dark ? dark + '\n' : ''}\n`;
  });
  navigator.clipboard.writeText(text).then(() => {
    const btn = document.getElementById('copyBtn');
    btn.textContent = '✅ 已复制';
    setTimeout(() => btn.textContent = '📋 复制全文', 2000);
  });
}

